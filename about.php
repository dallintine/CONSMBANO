                    <p>College of Nursing Sciences Mbano <b>(CONSMBANO)</b> commenced as a School of Nursing in <b>1983</b> under the proprietorship of the then Catholic Bishop of Okigwe Diocese, Most <b>Rev. Dr. Anthony Ilonu.</b>, with <b>Rev. Sr. Mary Thomas Anyanwu</b>, IHM, as the pioneer Principal. To the glory of God, in <b>Nov 2019,</b> the institution transmuted into a College of Nursing Sciences. Till date, the institution runs as a private tertiary institution, under the management of the Sisters of the Immaculate Heart of Mary, Mother of Christ, Nigeria, with <b>Rev. Sr. Dr. Mary Laurette Madu</b>, as the incumbent Provost.</p>

                    <p>The institution was founded out of the need created by the devastating effects of the Nigeria civil war, which ended in <i>1970.</i> There was the dearth of manpower for quality responsive healthcare providers to take care of the victims of the numerous outbreaks of infectious diseases caused by the degraded post civil war environments.</p>

                    <p>The institution received its statutory foundation under <b>Rev. Sr. Mary Laurette Madu,</b> who registered it with the Corporate Affairs Commission <b>(CAC) in 2017.</b> In the same year, the Imo State House of Assembly passed the law establishing the School on the <b>15th of June, 2017</b>, which further gave the Institution its legal backing, with the support of the Local Ordinary, <b>Most Rev. Dr. Solomon Amamchukwu Amatu,</b> the current catholic Bishop of Okigwe Diocese</p>

                    <p>As a School of Nursing, the institution had only one department &ndash; General Nursing, which prepared her graduates for the award of <b>diploma certificate (Registered Nurse)</b>  by the Nursing and Midwifery Council of Nigeria <b>(NMCN)</b></p>

                    <p>Following the successful accreditation exercise by the <b>NMCN in August, 2018,</b> the School was awarded full accreditation status, with annual student intake of seventy-five <b>(75)</b>.</p>

                    <p>To the glory of God, and in line with the nursing education reform in Nigeria, the College in <b>March 2019</b>, hosted the accreditation visitors from the West African Health Examination Board <b>(WAHEB)</b>, to run Public Health Nursing Programme, and she emerged successful. Similarly, she hosted another accreditation panel from the <b>NMCN</b> in September, 2019, for <b>Midwifery Programme</b> and recorded yet another resounding success.</p>

                    <p>The institution is adequately staffed in line with regulations of <b>NMCN</b>, <b>WAHEB</b> and <b>NBTE</b>.</p>

                    <p>The college boasts of adequate hostel accommodation for male and female students, transport facility and modern security facility.</p>


